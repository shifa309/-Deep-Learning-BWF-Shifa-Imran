def make_shirt(size="large", message="I love Python"):
    print("Size "  + size + " t-shirt with the message \"" + message + "\" on it.")

#large shirt with the default message
make_shirt()

#medium shirt with the default message
make_shirt(size="medium")

#small shirt with a custom message
make_shirt(size="small", message="AI/ML is the future!")

